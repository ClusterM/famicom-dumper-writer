static void start_app(void)
{
  // App start address
  void (*jmp_to_app)(void) = (void (*)(void))(*((volatile uint32_t*)(APP_ADDRESS + 4)));
  // Vector table
  SCB->VTOR = APP_ADDRESS;
  // Stack pointer (to RAM) for USER app in this address
  __set_MSP(*((volatile uint32_t*) APP_ADDRESS));
  jmp_to_app();

  // Should not execute
  while (1)
  {}
}

