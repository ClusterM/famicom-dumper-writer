/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  // Pre-init
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_TIM5_Init();
  MX_TIM2_Init();
  write_hardware_version();
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
  HAL_Delay(10);
  // If IRQ pin not grounded - start app
#ifndef DEBUG
  if (HAL_GPIO_ReadPin(IRQ_GPIO_Port, IRQ_Pin)) start_app();
#endif
  // Green LED
  led_green();
  // Need to release pin during one second
  HAL_Delay(1000);
  // If IRQ pin not released...
  if (!HAL_GPIO_ReadPin(IRQ_GPIO_Port, IRQ_Pin))
  {
    // Turn off the LED
    led_off();
    HAL_Delay(50);
    // Start app
    start_app();
  }
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USB_DEVICE_Init();
  MX_DMA_Init();
  MX_TIM5_Init();
  MX_FATFS_Init();
  MX_TIM2_Init();
  MX_TIM1_Init();
  /* USER CODE BEGIN 2 */
  // Microsecond counter
  HAL_TIM_Base_Start(&htim1);
  // M2
  HAL_TIM_Base_Start(&htim2);
  // Yellow LED
  led_yellow();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    // Check if last write operation was long time ago
    if (last_write_ts && (HAL_GetTick() - last_write_ts >= WRITE_TO_FLASH_TIME))
    {
      FILINFO fno;
      int usb_stopped = 0; // to prevent double deinit
      if (find_svf_file(&fno))
      {
        // White LED
        led_white();
        // Disable USB MSD
        USBD_DeInit(&hUsbDeviceFS);
        usb_stopped = 1;
        // Write firmware
        write_cpld(&fno);
        // Turn off the LED
        led_green();
        HAL_Delay(50);
        // Stop
      }
      if (find_bin_file(&fno))
      {
        // White LED
        led_white();
        // Disable USB MSD
        if (!usb_stopped) USBD_DeInit(&hUsbDeviceFS);
        // Write firmware
        write_firmware(&fno);
        // Turn off the LED
        led_green();
        HAL_Delay(50);
        // Stop
        __disable_irq();
        while (1)
        {
        }
      }

      last_write_ts = 0;
    }
    HAL_Delay(100);
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

